package JavaSemester1.Pertemuan7;

public class PolaKotak {
    public static void main(String[] args) {
        int tinggi = 3;
        int lebar = 5;

        for (int i = 1; i <= tinggi; i++) {

            for (int j = 1; j <= lebar; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
        
    }
    
}
