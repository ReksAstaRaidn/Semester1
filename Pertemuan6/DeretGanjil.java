package JavaSemester1.Pertemuan6;

public class DeretGanjil {
    public static void main(String[] args) {
        
        System.out.println("Program Deret Ganjil");

        for (int i = 1; i <= 30; i++) {
            
            if (i % 2 != 0) {
                System.out.println(i + " ");
            }
        }
        System.out.print("Ini adalah bilangan ganjil dari 1-30");

    }


}
