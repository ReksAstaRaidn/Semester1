package JavaSemester1.Pertemuan6;

public class PerulanganDasar {
    public static void main(String[] args) {

        System.out.println("===Perulangan Maju===");
        for (int i = 1; i <= 5; i++) {
            System.out.println("Putaran ke-" + i);
        }
        System.out.println("===Perulangan Mundur===");
        for (int j = 5; j >= 1; j--) {
            System.out.println("Putaran ke-" + j);
        }
    
    }
}
